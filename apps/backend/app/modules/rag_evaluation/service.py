import asyncio
import logging
from datetime import datetime
from typing import Optional

from bson import ObjectId
from fastapi import HTTPException
from google import genai
from pymongo.asynchronous.collection import AsyncCollection

from app.core.config import settings
from app.core.genai_adapter import generate_text_async, get_client, with_thinking
from app.db.client import DatabaseClient
from app.modules.rag_evaluation.dto import (
    RAGEvaluationResponse,
    RAGStatsResponse,
    RateAnswerRequest,
)

logger = logging.getLogger(__name__)

FAITHFULNESS_PROMPT = """\
You are a RAG faithfulness evaluator. Rate whether the following answer is
faithfully grounded in the provided context.

--- CONTEXT (retrieved by vector search) ---
{context}

--- ANSWER (generated by LLM) ---
{answer}

Rate the faithfulness from 0.0 to 1.0:
- 1.0 = Every claim in the answer is supported by the context
- 0.5 = Some claims supported, some not
- 0.0 = The answer is completely unfounded or hallucinated

Return ONLY a number between 0.0 and 1.0. No explanation, no markdown.
"""


class RAGEvaluationService:
    """Tracks RAG quality metrics: user ratings, latency, faithfulness scores.

    Every query/study-buddy interaction gets logged automatically from the
    respective services. Users can rate answers, and a background task runs
    LLM-as-judge faithfulness evaluation.
    """

    def __init__(self, db_client: type[DatabaseClient]) -> None:
        self._db = db_client
    @property
    def _evaluations_collection(self) -> AsyncCollection:
        coll = self._db.rag_evaluations
        assert coll is not None, "Database not connected"
        return coll

    @property
    def _llm_client(self) -> genai.Client:
        return get_client()

    # ------------------------------------------------------------------
    # Log a RAG interaction
    # ------------------------------------------------------------------

    async def log_evaluation(
        self,
        user_id: str,
        query: str,
        answer: str,
        answer_source: str,
        retrieved_chunks: list[dict],
        latency_ms: int,
    ) -> str:
        """Log a RAG interaction (called by query or study buddy services).

        The faithfullness evaluation runs as a background asyncio task so it
        doesn't block the response.
        """
        # Store only chunk metadata (not full text) to avoid database bloat
        chunks_for_storage = [
            {
                "source_type": chunk.get("source_type", "unknown"),
                "score": chunk.get("score", 0),
            }
            for chunk in retrieved_chunks
        ]

        doc = {
            "user_id": ObjectId(user_id),
            "query": query,
            "answer": answer,
            "answer_source": answer_source,  # "query" | "study_buddy"
            "retrieved_chunks": chunks_for_storage,
            "latency_ms": latency_ms,
            "user_rating": None,
            "user_feedback": None,
            "faithfulness_score": None,
            "created_at": datetime.utcnow(),
        }

        result = await self._evaluations_collection.insert_one(doc)
        eval_id = str(result.inserted_id)

        # Fire-and-forget via asyncio task — doesn't block the response
        async def _run_faithfulness() -> None:
            try:
                score = await self._evaluate_faithfulness(retrieved_chunks, answer)
                if score is not None:
                    await self._evaluations_collection.update_one(
                        {"_id": result.inserted_id},
                        {"$set": {"faithfulness_score": score}},
                    )
                    logger.info("Faithfulness score for %s: %.3f", eval_id, score)
            except Exception as exc:
                logger.warning("Faithfulness eval failed for %s: %s", eval_id, exc)

        asyncio.create_task(_run_faithfulness())

        return eval_id

    # ------------------------------------------------------------------
    # User rating
    # ------------------------------------------------------------------

    async def rate_answer(
        self, eval_id: str, user_id: str, payload: RateAnswerRequest
    ) -> RAGEvaluationResponse:
        """Rate a RAG interaction (thumbs up/down with optional feedback)."""
        try:
            oid = ObjectId(eval_id)
        except Exception:
            raise HTTPException(status_code=400, detail="Invalid evaluation ID")

        doc = await self._evaluations_collection.find_one({"_id": oid})
        if doc is None:
            raise HTTPException(status_code=404, detail="Evaluation not found")

        if str(doc["user_id"]) != user_id:
            raise HTTPException(
                status_code=403,
                detail="You can only rate your own queries",
            )

        rating = payload.rating
        if rating not in (1, -1):
            raise HTTPException(
                status_code=400,
                detail="Rating must be 1 (thumbs up) or -1 (thumbs down)",
            )

        update: dict = {"user_rating": rating}
        if payload.feedback:
            update["user_feedback"] = payload.feedback

        await self._evaluations_collection.update_one(
            {"_id": oid},
            {"$set": update},
        )

        doc.update(update)
        return self._format_eval(doc)

    # ------------------------------------------------------------------
    # Stats
    # ------------------------------------------------------------------

    async def get_stats(self, user_id: str) -> RAGStatsResponse:
        """Get aggregate RAG quality metrics for the user."""
        coll = self._evaluations_collection
        query: dict = {"user_id": ObjectId(user_id)}

        # Total queries
        total = await coll.count_documents(query)

        if total == 0:
            return RAGStatsResponse(
                total_queries=0, total_rated=0, positive_rate=0.0,
                negative_rate=0.0, avg_latency_ms=0.0, avg_faithfulness=None,
                queries_with_answer=0, no_answer_count=0, source_breakdown={},
            )

        # Rated queries
        rated_cursor = coll.find({"user_id": ObjectId(user_id), "user_rating": {"$ne": None}})
        rated_count = await coll.count_documents(
            {"user_id": ObjectId(user_id), "user_rating": {"$ne": None}}
        )
        positive_count = await coll.count_documents(
            {"user_id": ObjectId(user_id), "user_rating": 1}
        )
        negative_count = await coll.count_documents(
            {"user_id": ObjectId(user_id), "user_rating": -1}
        )

        # Average latency
        pipeline_latency: list[dict] = [
            {"$match": {"user_id": ObjectId(user_id)}},
            {"$group": {"_id": None, "avg_latency": {"$avg": "$latency_ms"}}},
        ]
        latency_cursor = await coll.aggregate(pipeline_latency)
        latency_result = await latency_cursor.to_list(length=1)
        avg_latency = latency_result[0]["avg_latency"] if latency_result else 0.0

        # Average faithfulness
        pipeline_faith: list[dict] = [
            {"$match": {"user_id": ObjectId(user_id), "faithfulness_score": {"$ne": None}}},
            {"$group": {"_id": None, "avg_faith": {"$avg": "$faithfulness_score"}}},
        ]
        faith_cursor = await coll.aggregate(pipeline_faith)
        faith_result = await faith_cursor.to_list(length=1)
        avg_faithfulness = faith_result[0]["avg_faith"] if faith_result else None

        # Source breakdown
        pipeline_sources: list[dict] = [
            {"$match": {"user_id": ObjectId(user_id)}},
            {"$group": {"_id": "$answer_source", "count": {"$sum": 1}}},
        ]
        source_cursor = await coll.aggregate(pipeline_sources)
        source_breakdown: dict[str, int] = {}
        async for doc in source_cursor:
            source_breakdown[str(doc["_id"])] = doc["count"]

        # No-answer count
        no_answer = await coll.count_documents(
            {"user_id": ObjectId(user_id), "answer": ""}
        )

        positive_rate = (positive_count / rated_count * 100) if rated_count > 0 else 0.0
        negative_rate = (negative_count / rated_count * 100) if rated_count > 0 else 0.0

        return RAGStatsResponse(
            total_queries=total,
            total_rated=rated_count,
            positive_rate=round(positive_rate, 1),
            negative_rate=round(negative_rate, 1),
            avg_latency_ms=round(avg_latency, 1),
            avg_faithfulness=round(avg_faithfulness, 4) if avg_faithfulness else None,
            queries_with_answer=total - no_answer,
            no_answer_count=no_answer,
            source_breakdown=source_breakdown,
        )

    # ------------------------------------------------------------------
    # List evaluations
    # ------------------------------------------------------------------

    async def list_evaluations(
        self, user_id: str, skip: int = 0, limit: int = 50,
        min_rating: Optional[int] = None,
    ) -> list[RAGEvaluationResponse]:
        """List the user's RAG evaluations, most recent first."""
        query: dict = {"user_id": ObjectId(user_id)}
        if min_rating is not None:
            query["user_rating"] = min_rating

        cursor = (
            self._evaluations_collection
            .find(query)
            .sort("created_at", -1)
            .skip(skip)
            .limit(limit)
        )

        return [self._format_eval(doc) async for doc in cursor]

    # ------------------------------------------------------------------
    # LLM-as-judge faithfulness evaluation
    # ------------------------------------------------------------------

    async def _evaluate_faithfulness(
        self, retrieved_chunks: list[dict], answer: str
    ) -> Optional[float]:
        """Use Gemini to evaluate answer faithfulness against retrieved context.

        Runs on a thread pool so it doesn't block the event loop.
        Returns 0.0-1.0 or None if evaluation fails.
        """
        if not answer or not retrieved_chunks:
            return None

        # Format context for the evaluator
        context_lines = []
        for i, chunk in enumerate(retrieved_chunks[:5], 1):  # Top 5 chunks
            text = chunk.get("text", "")[:2000]
            context_lines.append(f"[Chunk {i}]\n{text}")
        context = "\n\n".join(context_lines)

        prompt = FAITHFULNESS_PROMPT.format(context=context, answer=answer)

        try:
            raw = await generate_text_async(
                prompt,
                model=settings.gemini_model,
                config=with_thinking(),  # type: ignore[arg-type]
            )
            raw = raw.strip() if raw else ""
            score = float(raw)
            return max(0.0, min(1.0, score))  # Clamp to [0, 1]
        except (ValueError, Exception) as exc:
            logger.warning("Faithfulness eval error: %s", exc)
            return None

    # ------------------------------------------------------------------
    # Admin — all users
    # ------------------------------------------------------------------

    async def get_admin_stats(self) -> RAGStatsResponse:
        """Get aggregate RAG quality metrics across **all** users.

        Admin-only.  Same shape as ``get_stats()`` but unfiltered.
        """
        coll = self._evaluations_collection

        total = await coll.count_documents({})

        if total == 0:
            return RAGStatsResponse(
                total_queries=0, total_rated=0, positive_rate=0.0,
                negative_rate=0.0, avg_latency_ms=0.0, avg_faithfulness=None,
                queries_with_answer=0, no_answer_count=0, source_breakdown={},
            )

        rated_count = await coll.count_documents({"user_rating": {"$ne": None}})
        positive_count = await coll.count_documents({"user_rating": 1})
        negative_count = await coll.count_documents({"user_rating": -1})

        # Average latency
        pipeline_latency: list[dict] = [{"$group": {"_id": None, "avg_latency": {"$avg": "$latency_ms"}}}]
        latency_cursor = await coll.aggregate(pipeline_latency)
        latency_result = await latency_cursor.to_list(length=1)
        avg_latency = latency_result[0]["avg_latency"] if latency_result else 0.0

        # Average faithfulness
        pipeline_faith: list[dict] = [
            {"$match": {"faithfulness_score": {"$ne": None}}},
            {"$group": {"_id": None, "avg_faith": {"$avg": "$faithfulness_score"}}},
        ]
        faith_cursor = await coll.aggregate(pipeline_faith)
        faith_result = await faith_cursor.to_list(length=1)
        avg_faithfulness = faith_result[0]["avg_faith"] if faith_result else None

        # Source breakdown
        pipeline_sources: list[dict] = [
            {"$group": {"_id": "$answer_source", "count": {"$sum": 1}}},
        ]
        source_cursor = await coll.aggregate(pipeline_sources)
        source_breakdown: dict[str, int] = {}
        async for doc in source_cursor:
            source_breakdown[str(doc["_id"])] = doc["count"]

        no_answer = await coll.count_documents({"answer": ""})

        positive_rate = (positive_count / rated_count * 100) if rated_count > 0 else 0.0
        negative_rate = (negative_count / rated_count * 100) if rated_count > 0 else 0.0

        return RAGStatsResponse(
            total_queries=total,
            total_rated=rated_count,
            positive_rate=round(positive_rate, 1),
            negative_rate=round(negative_rate, 1),
            avg_latency_ms=round(avg_latency, 1),
            avg_faithfulness=round(avg_faithfulness, 4) if avg_faithfulness else None,
            queries_with_answer=total - no_answer,
            no_answer_count=no_answer,
            source_breakdown=source_breakdown,
        )

    async def list_admin_evaluations(
        self,
        user_id: Optional[str] = None,
        skip: int = 0,
        limit: int = 50,
        min_rating: Optional[int] = None,
    ) -> list[RAGEvaluationResponse]:
        """List evaluations across all users, optionally filtered.

        Admin-only.  Pass ``user_id`` to scope to a single user.
        """
        query: dict = {}
        if user_id is not None:
            try:
                query["user_id"] = ObjectId(user_id)
            except Exception:
                raise HTTPException(status_code=400, detail="Invalid user_id format")
        if min_rating is not None:
            query["user_rating"] = min_rating

        cursor = (
            self._evaluations_collection
            .find(query)
            .sort("created_at", -1)
            .skip(skip)
            .limit(limit)
        )

        return [self._format_eval(doc) async for doc in cursor]

    # ------------------------------------------------------------------
    # Helper
    # ------------------------------------------------------------------

    @staticmethod
    def _format_eval(doc: dict) -> RAGEvaluationResponse:
        return RAGEvaluationResponse(
            id=str(doc["_id"]),
            query=doc["query"],
            answer=doc["answer"],
            answer_source=doc["answer_source"],
            latency_ms=doc["latency_ms"],
            user_rating=doc.get("user_rating"),
            user_feedback=doc.get("user_feedback"),
            faithfulness_score=doc.get("faithfulness_score"),
            created_at=doc["created_at"],
        )
